// ─── Context ────────────────────────────────────────────────────────────────────

type I18nContextType = {
  // Primary API
  lang: Lang;
  setLang: (l: Lang) => void;
  t: (key: string) => string;
  // Backward-compat aliases (used by Sidebar, SupplierShell, etc.)
  locale: Lang;
  setLocale: (l: Lang) => void;
  dir: "ltr" | "rtl";
};

const I18nContext = createContext<I18nContextType>({
  lang: "fr",
  setLang: () => {},
  t: (key) => key,
  locale: "fr",
  setLocale: () => {},
  dir: "ltr",
});

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>("fr");

  useEffect(() => {
    const saved = localStorage.getItem("fulflo_lang") as Lang;
    if (saved && ["fr", "en", "de", "ar"].includes(saved)) {
      setLangState(saved);
      document.documentElement.dir = saved === "ar" ? "rtl" : "ltr";
    } else {
      setLangState("fr");
      localStorage.setItem("fulflo_lang", "fr");
      document.documentElement.dir = "ltr";
    }
  }, []);

  const setLang = (l: Lang) => {
    setLangState(l);
    localStorage.setItem("fulflo_lang", l);
    localStorage.setItem("fulflo_locale", l);
    document.documentElement.dir = l === "ar" ? "rtl" : "ltr";
  };

  // Memoize so context consumers only re-render when lang actually changes
  const ctx = useMemo<I18nContextType>(() => ({
    lang,
    setLang,
    t: (key: string) => translations[lang][key] ?? translations.fr[key] ?? key,
    // Backward compat aliases
    locale: lang,
    setLocale: setLang,
    dir: lang === "ar" ? "rtl" : "ltr",
  // eslint-disable-next-line react-hooks/exhaustive-deps
  }), [lang]);

  return (
    <I18nContext.Provider value={ctx}>
      {children}
    </I18nContext.Provider>
  );
}

export function useI18n() {
  return useContext(I18nContext);
}

